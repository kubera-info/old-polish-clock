static const float fScale( 0.065f );
static const float fDeph = .007f;

void TransformHook( XMFLOAT3 & NewVal )
{
	XMFLOAT3 tmp;
	tmp.x = NewVal.x;
	tmp.y = NewVal.y;
	tmp.z = NewVal.z;

	XMVECTOR res = XMVector3Transform(
		XMLoadFloat3( &tmp ),
		XMMatrixRotationY( M_PI / 2.0f ) * XMMatrixScaling( fScale, fScale, fScale ) );
	XMStoreFloat3( &tmp, res );

	NewVal.x = tmp.x;
	NewVal.y = tmp.y;
	NewVal.z = tmp.z;
}

void TransformDephHook( XMFLOAT3 & NewVal )
{
	NewVal.x -= fDeph;
}

void TransformFinalHook( XMFLOAT3 & NewVal, const float Y )
{
	NewVal.x += .767f;
	NewVal.y += Y;
	NewVal.z += .13f;
}

